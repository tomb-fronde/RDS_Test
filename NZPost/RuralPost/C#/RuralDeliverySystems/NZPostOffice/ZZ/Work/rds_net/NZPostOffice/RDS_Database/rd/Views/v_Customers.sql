CREATE VIEW [rd].[v_Customers]
AS
SELECT     rd.customer_address_moves.adr_id, rd.customer_address_moves.cust_id, rd.rds_customer.cust_title, rd.rds_customer.cust_surname_company, 
                      rd.rds_customer.cust_initials
FROM         rd.customer_address_moves INNER JOIN
                      rd.rds_customer ON rd.customer_address_moves.cust_id = rd.rds_customer.cust_id
WHERE     (rd.rds_customer.master_cust_id IS NULL) AND (rd.customer_address_moves.move_out_date IS NULL)
GO
